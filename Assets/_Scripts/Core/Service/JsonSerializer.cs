using UnityEngine;

namespace AjaxNguyen.Core.Service
{
    public class JsonSerializer 
    {
        public string Serialize<T>(T obj)
        {
            return JsonUtility.ToJson(obj, true);
        }

        public T Deserialize<T>(string json)
        {
            return JsonUtility.FromJson<T>(json);
        }
    }
}